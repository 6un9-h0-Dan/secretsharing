package org.mitre.secretsharing.codec.lts;

public class PartfileException extends Exception {
	private static final long serialVersionUID = 0;
	
	public PartfileException() {
		super();
	}

	public PartfileException(String message, Throwable cause) {
		super(message, cause);
	}

	public PartfileException(String message) {
		super(message);
	}

	public PartfileException(Throwable cause) {
		super(cause);
	}

}
